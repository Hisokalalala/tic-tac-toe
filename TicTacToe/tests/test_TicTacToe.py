from unittest import TestCase
from TicTacToe.TicTacToe import turn


class Test(TestCase):
    def test_turn(self):
        print("○ ? ?\n? ? ?\n? ? ?\n")
        self.assertEqual(turn([["?", "?", "?"], ["?", "?", "?"], ["?", "?", "?"]], "M", 1, 1), "○ ? ?\n? ? ?\n? ? ?\n")
        self.assertEqual(turn([["?", "?", "?"], ["?", "?", "?"], ["?", "?", "?"]], "B", 2, 1), "? ? ?\n× ? ?\n? ? ?\n")
